#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0 && n * (-1) != n)
	{
		n = n * -1;
		ft_putchar_fd('-', fd);
	}
	if (n % 10 != n)
		ft_putnbr_fd(n / 10, fd);
	if (n % 10 > 0)
		ft_putchar_fd(n % 10 + 48, fd);
	else
		ft_putchar_fd(n % 10 * (-1) + 48, fd);
}
