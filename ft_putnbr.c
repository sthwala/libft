#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0 && n * (-1) != n)
	{
		n = n * -1;
		ft_putchar('-');
	}
	if (n % 10 != n)
		ft_putnbr(n / 10);
	if (n % 10 > 0)
		ft_putchar(n % 10 + 48);
	else
		ft_putchar(n % 10 * (-1) + 48);
}
